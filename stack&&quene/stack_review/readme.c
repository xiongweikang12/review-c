/*栈（stack)的定义与的特点*/
/*栈是一个特殊的线性表，是限定仅仅在一端（通常是表尾）*/
/*进行插入和删除操作的线性表*/

/*遵循后进先出原则（last in frist out)*/
/*相关概念*/
/*栈是仅仅在表尾（栈顶）进行插入，删除操作的线性表*/
/*表尾称为栈顶，表头称为栈底base*/

/*插入元素到栈顶（表尾）的操作，称为入栈 ，push*/
/*从栈顶删除最后一个元素的操作称为出栈, pop*/

/*逻辑结构：与同线性表相同，任为一对一关系*/
/*顺序栈或者链式栈存储都可以，但以顺序栈更为常见*/
/*只能在栈顶进行运算*/
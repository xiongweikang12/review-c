/*树形结构：结点之间有分支具有层次关系*/

/*

结点：数据元素以及指向子树的分支
结点的度：结点拥有的子树数量 （后继元素数目，分支数）
树的度：树种各节点的度的最大值
结点度为零：叶子结点（终端结点）
孩子结点，双亲结点
相同双亲的结点：兄弟节点
深度
深度相同不同双亲的结点称为堂兄弟

有序树：树中结点的各个子树从左到右有次序 ABCD
无序树：树中结点的各个子树无序
森林：是m棵互不相交的树的集合（将根节点删除就变成了森林）

*/

/*

二叉树的定义：
1.每个结点最多有两个孩子（二叉树中不存在度大于2的结点）
2.结点子树有左右之分，其次序不能颠倒
3.二叉树可以是空集合，根可以有空的左子树，或右子树

*/


/*链式存储结构的优点:*/
/*结点空间可以动态申请和释放*/
/*数据元素的逻辑次序靠结点的指针来指示，插入和删除时不需要移动数据元素*/

/*链式存储结构的缺点*/
/*存储密度小，每个结点的指针域需要额外的占用空间，当结点数据域所占
字节不多时，指针域所占的存储空间比重显得很大*/

//存储密度越大，存储利用率越高。


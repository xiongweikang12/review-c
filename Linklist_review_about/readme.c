/*链表表示方式与实现*/
/*头指针，头结点，首元结点*/

/*

头指针：是指向链表中第一个结点的指针（通常链表就是用这样一个指针表示的）
首元结点：存储第一个元素的结点（a1）
头结点：是在链表首元结点之前附设的一个结点

*/

/*

头结点的好处：

1.便于首元结点的操作，与其他位置无疑，对于有头结点（储存了a1的指针域，表示）
2.便于空表与非空表的统一，简单来说就是头指针都是指向非空（头结点），
头结点的指针域决定是否为空表。


*/

